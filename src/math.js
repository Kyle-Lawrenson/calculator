let add = (num1, num2) => {
    let result = num1 + num2;
    return result;
};

let subtract = (num1, num2) => {
    let result = num1 - num2;
    return result;
};

let multiply = (num1, num2) => {
    let result = num1 * num2;
    return result;
};

let divide = (num1, num2) => {
    let result = num1 / num2;
    return result;
};

module.exports = {add, subtract, multiply, divide};